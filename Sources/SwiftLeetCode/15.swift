  public static func threeSum(_ nums: [Int]) -> [[Int]] {
    switch nums.count {
    case 0, 1, 2:
      return []
    default:
      var solution_indexes: [Int: [(Int, Int, Int)]] = .init()

      for i in (0...nums.count - 3) {
        let num = nums[i]

        if solution_indexes[num] != nil {
          continue
        }

        let two_sum_indexes = twoSum(nums[(i + 1)...], 0 - nums[i])

        var solutions_for_this: [(Int, Int, Int)] = .init()
        solutions_for_this.reserveCapacity(two_sum_indexes.count)

        for (idx_b, idx_c) in two_sum_indexes {
          solutions_for_this.append((i, i + 1 + idx_b, i + 1 + idx_c))
        }

        solution_indexes[num] = solutions_for_this
      }

      return Array(
        Set(
          solution_indexes.values.compactMap({ (solutions: [(Int, Int, Int)]) -> [[Int]]? in
            if solutions.isEmpty {
              return nil
            }

            return solutions.map {
              let (a, b, c) = $0

              return [nums[a], nums[b], nums[c]].sorted()
            }
          }).joined()))
    }
  }

  public static func twoSum(_ nums: ArraySlice<Int>, _ target: Int) -> [(Int, Int)] {
    var num_to_indexes: [Int: [Int]] = .init(minimumCapacity: nums.count)
    var solutions: [(Int, Int)] = []

    solutions.reserveCapacity(nums.count)

    for (this_idx, num) in nums.enumerated() {
      if let all_possible = num_to_indexes[target - num] {
        for that_idx in all_possible {
          solutions.append((this_idx, that_idx))
        }
      } else {
        var indexes = num_to_indexes[num] ?? []
        indexes.append(this_idx)
        num_to_indexes[num] = indexes
      }
    }

    return solutions
  }
