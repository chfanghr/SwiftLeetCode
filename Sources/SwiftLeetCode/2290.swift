public class MinimumObstacleRemovalToReachCorner {
    public static func minimumObstacles(_ grid: [[Int]]) -> Int {
        var nextPositions = Deque<(Position, Int /* cost */)>()
        var visited = Set<Position>()

        let initialPosition = Position(rowIndex: 0, colIndex: 0)
        nextPositions.append((initialPosition, accessGrid(grid, initialPosition).cost))
        let endPosition = Position(rowIndex: grid.count - 1, colIndex: grid.first!.count - 1)

        while let (position, cost) = nextPositions.popFront() {
            if position == endPosition {
                return cost
            }
            if visited.contains(position) {
                continue
            }
            visited.insert(position)
            for (position, cell) in nextPossiblePositionsOnGrid(grid, position) {
                let cost = cost + cell.cost
                if cell.isObstacle {
                    nextPositions.append((position, cost))
                } else {
                    nextPositions.prepend((position, cost))
                }
            }
        }

        fatalError()
    }

    struct Position: Hashable, Equatable {
        let rowIndex: Int
        let colIndex: Int
    }

    enum Cell {
        case empty
        case obstacle

        var cost: Int {
            switch self {
            case .empty: 0
            case .obstacle: 1
            }
        }

        var isObstacle: Bool {
            self == .obstacle
        }
    }

    static func accessGrid(_ grid: [[Int]], _ position: Position) -> Cell {
        switch grid[position.rowIndex][position.colIndex] {
        case 0:
            .empty
        case 1:
            .obstacle
        default:
            fatalError()
        }
    }

    static func nextPossiblePositionsOnGrid(_ grid: [[Int]], _ thisPosition: Position) -> [(
        Position, Cell
    )] {
        let rows = grid.count
        let cols = grid.first!.count

        return [(1, 0), (-1, 0), (0, 1), (1, 0)].compactMap { (offsets) in
            let rowIndex = thisPosition.rowIndex + offsets.0
            let colIndex = thisPosition.colIndex + offsets.1
            let position = Position(rowIndex: rowIndex, colIndex: colIndex)

            return if (0..<rows).contains(rowIndex) && (0..<cols).contains(colIndex) {
                (position, accessGrid(grid, position))
            } else {
                nil
            }
        }
    }

    struct Deque<T> {
        private class Node {
            internal init(_ val: T, next: Node? = nil) {
                self.val = val
                self.next = next
            }

            let val: T
            var next: Node?
        }

        private var head: Node? = nil
        private var tail: Node? = nil

        mutating func prepend(_ val: T) {
            let node = Node(val, next: self.head)
            self.head = node
            if self.tail == nil {
                self.tail = node
            }
        }

        mutating func append(_ val: T) {
            let node = Node(val)
            if let oldTail = self.tail {
                oldTail.next = node
            } else {
                self.head = node
            }
            self.tail = node
        }

        mutating func popFront() -> T? {
            guard let oldHead = self.head else {
                return nil
            }
            self.head = oldHead.next
            if self.head == nil {
                self.tail = nil
            }
            return oldHead.val
        }
    }

